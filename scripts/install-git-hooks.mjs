import { chmodSync, mkdirSync, writeFileSync } from 'node:fs';
import { existsSync } from 'node:fs';
import { resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO_ROOT = resolve(fileURLToPath(import.meta.url), '..', '..');
const GIT_DIR = resolve(REPO_ROOT, '.git');

if (!existsSync(GIT_DIR)) {
  process.stdout.write('No .git directory detected — skipping git hook installation.\n');
  process.exit(0);
}

const HOOKS_DIR = resolve(GIT_DIR, 'hooks');
mkdirSync(HOOKS_DIR, { recursive: true });

const PRECOMMIT_PATH = resolve(HOOKS_DIR, 'pre-commit');

const PRECOMMIT_SCRIPT = `#!/usr/bin/env sh
# Auto-generated by scripts/install-git-hooks.mjs
# Runs the same AI-tooling checks Claude Code runs interactively, so other agents
# (Cursor, Copilot, Codex, Aider, Cline, Continue, Windsurf) and unattended commits
# get the same enforcement.
exec node "$(git rev-parse --show-toplevel)/scripts/precommit.mjs"
`;

writeFileSync(PRECOMMIT_PATH, PRECOMMIT_SCRIPT, { encoding: 'utf8' });

try {
  chmodSync(PRECOMMIT_PATH, 0o755);
} catch {
  // chmod may fail on Windows; git for Windows handles the executable bit on its own.
}

process.stdout.write(`Installed git pre-commit hook at ${PRECOMMIT_PATH}\n`);
