// Generates AI assistant rule adapters from .claude/skills/*/SKILL.md.
//
// Source of truth: .claude/skills/<name>/SKILL.md (Ring format YAML + body).
// Targets:
//   - .cursor/rules/<name>.mdc          (Cursor)
//   - .windsurf/rules/<name>.md         (Windsurf)
//   - .clinerules/<name>.md             (Cline)
//   - .continue/rules/<name>.md         (Continue.dev)
//   - CONVENTIONS.md                    (Aider, consolidated)
//
// Run: node scripts/sync-rules.mjs
// Cross-platform (Node 24+, no shell, no extra deps).

