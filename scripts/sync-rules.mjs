function generateAiderConventions(skills) {
  const targetPath = join(REPO_ROOT, 'CONVENTIONS.md');

  const tableRows = skills.map((skill) => `- **${skill.name}** — ${shortDescription(skill)}`);

  const sections = skills.map((skill) => {
    const triggerLine = skill.frontmatter.trigger
      ? `\n**When it applies:**\n\n${skill.frontmatter.trigger}\n`
      : '';
    const skipLine = skill.frontmatter.skip_when
      ? `\n**When to skip:**\n\n${skill.frontmatter.skip_when}\n`
      : '';
    return [
      `## ${skill.name}`,
      '',
      `> ${shortDescription(skill)}`,
      '',
      triggerLine,
      skipLine,
      buildRuleBody(skill),
    ]
      .filter((part) => part.length > 0)
      .join('\n');
  });

  const lines = [
    ...GENERATED_HEADER_LINES,
    '',
    '# Conventions (Aider / generic agents)',
    '',
    '> AUTO-GENERATED. Edit `.claude/skills/<name>/SKILL.md` and run `npm run sync:rules`.',
    '',
    'Authoritative source: [`.claude/skills/`](.claude/skills/).',
    'For a higher-level overview, see [`AGENTS.md`](AGENTS.md) and [`CLAUDE.md`](CLAUDE.md).',
    '',
    '## Skill Index',
    '',
    ...tableRows,
    '',
    '---',
    '',
    sections.join('\n\n---\n\n'),
  ];

  writeFileSync(targetPath, lines.join('\n') + '\n', 'utf8');
}

function buildRuleBody(skill) {
  const triggerSection = skill.frontmatter.trigger
    ? `**Trigger:**\n\n${skill.frontmatter.trigger}\n\n`
    : '';
  const skipSection = skill.frontmatter.skip_when
    ? `**Skip when:**\n\n${skill.frontmatter.skip_when}\n\n`
    : '';
  return `${triggerSection}${skipSection}${skill.body.trim()}`;
}

function writeIndex(targetDir, fileName, skills, extension) {
  const lines = [
    ...GENERATED_HEADER_LINES,
    '',
    '# Generated rules',
    '',
    '> AUTO-GENERATED. Edit `.claude/skills/<name>/SKILL.md` and run `npm run sync:rules`.',
    '',
  ];
  for (const skill of skills) {
    lines.push(`- [${skill.name}](${skill.name}${extension}) — ${shortDescription(skill)}`);
  }
  writeFileSync(join(targetDir, fileName), lines.join('\n') + '\n', 'utf8');
}

function escapeYamlScalar(value) {
  if (value.includes(':') || value.includes('#') || value.includes('"')) {
    const escaped = value.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
    return `"${escaped}"`;
  }
  return value;
}

function main() {
  const skillNames = listSkillDirectories();
  if (skillNames.length === 0) {
    process.stderr.write('[sync-rules] No skills found in .claude/skills/. Aborting.\n');
    process.exit(1);
  }

  const skills = skillNames.map(readSkill);

  generateCursorAdapter(skills);
  generateWindsurfAdapter(skills);
  generateClineAdapter(skills);
  generateContinueAdapter(skills);
  generateAiderConventions(skills);

  process.stdout.write(
    `[sync-rules] Generated adapters for ${skills.length} skills:\n` +
      `  - .cursor/rules/       (${skills.length} files)\n` +
      `  - .windsurf/rules/     (${skills.length} files)\n` +
      `  - .clinerules/         (${skills.length} files)\n` +
      `  - .continue/rules/     (${skills.length} files)\n` +
      `  - CONVENTIONS.md       (consolidated)\n`,
  );
}

main();
