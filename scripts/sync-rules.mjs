const DEFAULT_CONFIG = {
  alwaysApply: false,
  globs: [],
  description: '',
};

function listSkillDirectories() {
  let entries = [];
  try {
    entries = readdirSync(SKILLS_DIR);
  } catch (error) {
    process.stderr.write(`[sync-rules] Cannot read ${SKILLS_DIR}: ${error.message}\n`);
    process.exit(1);
  }

  const skillNames = [];
  for (const entry of entries) {
    const fullPath = join(SKILLS_DIR, entry);
    if (statSync(fullPath).isDirectory()) {
      const skillFile = join(fullPath, 'SKILL.md');
      try {
        statSync(skillFile);
        skillNames.push(entry);
      } catch {
        // skip directories without SKILL.md
      }
    }
  }
  return skillNames.sort();
}

function readSkill(skillName) {
  const filePath = join(SKILLS_DIR, skillName, 'SKILL.md');
  const raw = readFileSync(filePath, 'utf8');
  const frontmatter = extractFrontmatter(raw);
  const body = extractBody(raw);
  return {
    name: skillName,
    frontmatter,
    body,
    config: ADAPTER_CONFIG[skillName] ?? DEFAULT_CONFIG,
  };
}

function extractFrontmatter(raw) {
  const trimmed = raw.replace(/^﻿/, '');
  if (!trimmed.startsWith('---')) {
    return { name: '', description: '', trigger: '', skip_when: '' };
  }
  const closingIndex = trimmed.indexOf('\n---', 3);
  if (closingIndex === -1) {
    return { name: '', description: '', trigger: '', skip_when: '' };
  }
  const header = trimmed.slice(3, closingIndex);
  return parseSimpleYaml(header);
}

function extractBody(raw) {
  const trimmed = raw.replace(/^﻿/, '');
  if (!trimmed.startsWith('---')) return trimmed;
  const closingIndex = trimmed.indexOf('\n---', 3);
  if (closingIndex === -1) return trimmed;
  const afterClose = trimmed.slice(closingIndex + 4);
  return afterClose.replace(/^\r?\n/, '');
}

function parseSimpleYaml(text) {
  const result = { name: '', description: '', trigger: '', skip_when: '' };
  const lines = text.split(/\r?\n/);

  let lineIndex = 0;
  while (lineIndex < lines.length) {
    const line = lines[lineIndex];
    const inlineMatch = line.match(/^([a-zA-Z_][a-zA-Z0-9_]*):\s*(.*)$/);
    if (!inlineMatch) {
      lineIndex += 1;
      continue;
    }
    const key = inlineMatch[1];
    const valuePart = inlineMatch[2];

    if (valuePart === '|' || valuePart === '|-' || valuePart === '|+') {
      const collected = [];
      lineIndex += 1;
      while (lineIndex < lines.length) {
        const nextLine = lines[lineIndex];
        if (nextLine.length === 0) {
          collected.push('');
          lineIndex += 1;
          continue;
        }
        const indentMatch = nextLine.match(/^(\s+)(.*)$/);
        if (!indentMatch) break;
        collected.push(indentMatch[2]);
        lineIndex += 1;
      }
      result[key] = collected.join('\n').trim();
      continue;
    }

    if (valuePart.length > 0) {
      result[key] = valuePart.trim();
    }
    lineIndex += 1;
  }
  return result;
}

function shortDescription(skill) {
  if (skill.config.description) return skill.config.description;
  const fmDescription = skill.frontmatter.description || '';
  const firstLine = fmDescription.split('\n')[0]?.trim() || '';
  if (firstLine.length > 0) return firstLine;
  return `Skill: ${skill.name}`;
}

function clearGeneratedDir(absolutePath) {
  try {
    rmSync(absolutePath, { recursive: true, force: true });
  } catch {
    // ignore
  }
  mkdirSync(absolutePath, { recursive: true });
}

function ensureDir(absolutePath) {
  mkdirSync(absolutePath, { recursive: true });
}

function generateCursorAdapter(skills) {
  const targetDir = join(REPO_ROOT, '.cursor', 'rules');
  clearGeneratedDir(targetDir);

  for (const skill of skills) {
    const description = shortDescription(skill);
    const frontmatter = ['---'];

    if (skill.config.alwaysApply) {
      // Cursor "Always Apply": only alwaysApply + optional description.
      frontmatter.push(`description: ${escapeYamlScalar(description)}`);
      frontmatter.push('alwaysApply: true');
    } else if (skill.config.globs.length > 0) {
      // Cursor "Auto Attached": globs as comma-separated string (NOT array).
      frontmatter.push(`description: ${escapeYamlScalar(description)}`);
      frontmatter.push(`globs: ${skill.config.globs.join(', ')}`);
      frontmatter.push('alwaysApply: false');
    } else {
      // Cursor "Agent Requested": description only, agent decides.
      frontmatter.push(`description: ${escapeYamlScalar(description)}`);
      frontmatter.push('alwaysApply: false');
    }

    frontmatter.push('---');

    const lines = [
      ...frontmatter,
      '',
      ...GENERATED_HEADER_LINES,
      '',
      `# ${skill.name}`,
      '',
      `> Source of truth: \`.claude/skills/${skill.name}/SKILL.md\``,
      '',
      buildRuleBody(skill),
    ];

    writeFileSync(join(targetDir, `${skill.name}.mdc`), lines.join('\n') + '\n', 'utf8');
  }

  writeIndex(targetDir, 'README.md', skills, '.mdc');
}

function generateWindsurfAdapter(skills) {
  const targetDir = join(REPO_ROOT, '.windsurf', 'rules');
  clearGeneratedDir(targetDir);

  for (const skill of skills) {
    const description = shortDescription(skill);
    const frontmatter = ['---'];

    if (skill.config.alwaysApply) {
      // Windsurf "always_on": full rule in every prompt; no globs needed.
      frontmatter.push('trigger: always_on');
      frontmatter.push(`description: ${escapeYamlScalar(description)}`);
    } else if (skill.config.globs.length > 0) {
      // Windsurf "glob": activates when Cascade reads/edits matching files.
      frontmatter.push('trigger: glob');
      frontmatter.push(`description: ${escapeYamlScalar(description)}`);
      frontmatter.push(`globs: ${skill.config.globs.join(', ')}`);
    } else {
      // Windsurf "model_decision": only description shown; model decides relevance.
      frontmatter.push('trigger: model_decision');
      frontmatter.push(`description: ${escapeYamlScalar(description)}`);
    }

    frontmatter.push('---');

    const lines = [
      ...frontmatter,
      '',
      ...GENERATED_HEADER_LINES,
      '',
      `# ${skill.name}`,
      '',
      `> Source of truth: \`.claude/skills/${skill.name}/SKILL.md\``,
      '',
      buildRuleBody(skill),
    ];

    writeFileSync(join(targetDir, `${skill.name}.md`), lines.join('\n') + '\n', 'utf8');
  }

  writeIndex(targetDir, 'README.md', skills, '.md');
}

